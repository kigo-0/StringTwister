package bean;

public class CharacterSet {
	
	private String AlphabetSet = "";
	
	private String UppercaseSet = "";
	
	private String LowercaseSet = "";
	
	private String NumberSet = "";
	
	private String SpecialCharacterSet = "";

	public String getAlphabetSet() {
		return AlphabetSet;
	}

	public void setAlphabetSet(String alphabetSet) {
		AlphabetSet = alphabetSet;
	}

	public String getUppercaseSet() {
		return UppercaseSet;
	}

	public void setUppercaseSet(String uppercaseSet) {
		UppercaseSet = uppercaseSet;
	}

	public String getLowercaseSet() {
		return LowercaseSet;
	}

	public void setLowercaseSet(String lowercaseSet) {
		LowercaseSet = lowercaseSet;
	}

	public String getNumberSet() {
		return NumberSet;
	}

	public void setNumberSet(String numberSet) {
		NumberSet = numberSet;
	}

	public String getSpecialCharacterSet() {
		return SpecialCharacterSet;
	}

	public void setSpecialCharacterSet(String specialCharacterSet) {
		SpecialCharacterSet = specialCharacterSet;
	}
	
	

}
